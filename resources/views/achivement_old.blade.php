              <p>
                Whereas, SANPA and BCGD-DIU share the same vision and objectives to promote public
                administration and governance research and education to contribute to inclusive development
                and accountable governance in South Asia and beyond;
              </p>
            <p>
              Whereas, SANPA and BCGD-DIU aim to strengthen academic and research collaborations by
              attracting scholars and practitioners from the South Asia region and beyond and become
              platforms of academics and practitioners further to promote public administration education,
              research, and capacity building;
            </p>
            <p>
              Therefore, SANPA and BCGD-DIU agree to forge partnerships in the form of a Memorandum of
              Understanding (MoU) under the following terms and conditions.
            </p>
           
            </div>
