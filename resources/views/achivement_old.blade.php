          <h4>Modalities of Collaboration</h4>
          <p>
            In this context, as both institutions are united by common interests and objectives in research
            and academic collaboration in areas of public administration as an academic discipline
            and a field of practice, public policy and governance and inclusive development, and they wish
            to maintain and promote collaboration that permit those mutual scholarly and academic ties
            and promote closer collaboration.
          </p>
          <p>
            <strong>SANPA and BCGD-DIU, therefore, agree to collaborate in the following areas:</strong>
          </p>
          <p>
            <strong>1.</strong>  Collaboration on research and publications on broader areas of process and practices of
            public administration, public policy, governance dynamics, development of course curricula
            catering to the need for changing dimensions of governance demand and deficit.
          </p>
          <p>
            <strong>2.</strong>  Joint research on dynamics and process of design and implementation issues of public
            policies and public administration practices and global development agenda such as the
            Agenda 2030;
          </p>
          <p>
            <strong>3.</strong>  Develop new case studies on public sector management, policy implementation, program
            management issues on the basis of the existing and forthcoming collaborative researches;
          </p>
          <p>
            <strong>4.</strong>  Institutional collaboration in organizing regional and international seminar, conference
            and workshop.
          </p>
          <p>
            <strong>5.</strong>  Jointly organizing workshops/ seminars and international conferences with any other
            national or international public or private institution/organisation;
          </p>
          <p>
            <strong>6.</strong> This agreement will govern the general framework for joint research, publications,
            arrangement of workshops, seminars, and conferences, and any other mutually agreed
            activities. Individual working agreement(s) for such collaboration, should they arise, will be
            executed for specific projects/ activities under this agreement.
          </p>
          <p>
            <strong>7.</strong>  The individual working agreement(s) referred to in paragraph 2 may be developed for, but
            need not be limited to, joint research and publications, arranging workshops, seminars,
            conferences, etc.
          </p>
          <p>
            <strong>8.</strong>  The cost of any joint activity shall be mutually determined.
          </p>
          <p>
            <strong>9.</strong> This Memorandum of Understanding (MoU) may be modified or extended by written consent
            of both parties.
          </p>
          <p>
            <strong>10.</strong>  Either Party may terminate this MOU by giving three months’ prior notice in writing to the
            other party
          </p>
          <p>
            <strong>11.</strong>  This Memorandum of Understanding (MoU) will become effective on the date above and
            remain for five years from that date. It may be renewed for an additional period by an exchange
            of letters between the parties.
